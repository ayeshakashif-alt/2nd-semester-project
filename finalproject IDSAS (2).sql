CREATE DATABASE idsas;
USE idsas;

CREATE TABLE countries(
country_code CHAR(3) PRIMARY KEY,
country_name VARCHAR(500) NOT NULL,
region VARCHAR(500) NOT NULL
);

CREATE TABLE indicators(
indicator_id INT PRIMARY KEY,
indicator_name VARCHAR(500) NOT NULL,
ind_description VARCHAR(1000)
);

CREATE TABLE debt_records(
country_code CHAR(3) NOT NULL,
indicator_id INT NOT NULL,
debt_year INT,
amount DECIMAL(20, 2) NOT NULL,
FOREIGN KEY (country_code) REFERENCES countries(country_code),
FOREIGN KEY (indicator_id) REFERENCES indicators(indicator_id)
);

CREATE TABLE users(
user_id INT PRIMARY KEY AUTO_INCREMENT,
user_name VARCHAR(200),
role ENUM('Viewer', 'Analyst', 'Admin') NOT NULL,
email VARCHAR(500) UNIQUE NOT NULL
);

CREATE TABLE principal_repayment(
repayment_id INT PRIMARY KEY,
country_code CHAR(3) NOT NULL,
debt_year INT NOT NULL,
amount DECIMAL(20, 2) NOT NULL,
FOREIGN KEY (country_code) REFERENCES countries (country_code)
);

CREATE TABLE regional_stats(
region_id INT PRIMARY KEY,
region VARCHAR(500),
total_debt DECIMAL(20, 2)
);

CREATE TABLE reports(
report_id INT PRIMARY KEY,
indicator_id INT NOT NULL,
user_id INT NOT NULL,
date_generated DATETIME NOT NULL,
type VARCHAR(500),
FOREIGN KEY (indicator_id) REFERENCES indicators (indicator_id),
FOREIGN KEY (user_id) REFERENCES users (user_id)

);

